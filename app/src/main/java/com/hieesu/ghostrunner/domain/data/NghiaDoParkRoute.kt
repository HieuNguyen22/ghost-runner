     * Approximate calculation of the total length of the loop in meters.
     */
    fun calculateLoopDistance(): Double {
        var distance = 0.0
        val metersPerDegree = 111_320.0
        for (i in 0 until loopPoints.size - 1) {
            val p1 = loopPoints[i]
            val p2 = loopPoints[i + 1]
            val dLat = (p2.latitude - p1.latitude) * metersPerDegree
            val dLng = (p2.longitude - p1.longitude) * metersPerDegree * cos(Math.toRadians(p1.latitude))
            distance += hypot(dLat, dLng)
        }
        
        // Connect last point back to first point to close the loop
        val p1 = loopPoints.last()
        val p2 = loopPoints.first()
        val dLat = (p2.latitude - p1.latitude) * metersPerDegree
        val dLng = (p2.longitude - p1.longitude) * metersPerDegree * cos(Math.toRadians(p1.latitude))
        distance += hypot(dLat, dLng)
        
        return distance
    }
